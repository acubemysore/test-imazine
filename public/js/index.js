// Getting width of one slider to scoll
let sliderWidth = parseFloat(
  getComputedStyle(document.querySelector(".blog-slider")).width
);

let blogCurrentSlide = 0;
let goingNextSlide = true;
let scrollLimit =
  window.screen.width <= 800 ? blogList.length : blogList.length - 2;

let sliding = true;
const nextBlogSlide = function () {
  if (!sliding) return;
  goingNextSlide = true;
  console.log(document.querySelectorAll(".blog-slide")[blogCurrentSlide]);
  if (blogCurrentSlide <= blogList.length - 1) {
    if (window.screen.width <= 800) {
      sliderWidth = parseFloat(
        getComputedStyle(
          document.querySelectorAll(".blog-slide")[blogCurrentSlide]
        ).width
      );
    } else {
      sliderWidth =
        parseFloat(
          getComputedStyle(
            document.querySelectorAll(".blog-slide")[blogCurrentSlide]
          ).width
        ) +
        parseFloat(getComputedStyle(document.querySelector(".col")).width) * 5;
    }
  }
  if (scrollLimit == blogCurrentSlide) return;

  blogSliderArrowRight.style.opacity = 0.6;
  blogSliderArrowLeft.style.opacity = 0.6;
  blogCurrentSlide++;
  // blogSlides[blogCurrentSlide].scrollIntoView();
  blogsListELe.scrollLeft += sliderWidth;
  if (blogCurrentSlide === scrollLimit)
    blogSliderArrowRight.style.opacity = 0.3;

  sliding = false;

  setTimeout(() => {
    sliding = true;
  }, 500);
};
const prevBlogSlide = function () {
  if (!sliding) return;
  goingNextSlide = true;
  if (!blogCurrentSlide < 0) {
    sliderWidth = parseFloat(
      getComputedStyle(
        document.querySelectorAll(".blog-slide")[blogCurrentSlide - 1]
      ).width
    );
  }
  if (blogCurrentSlide === 0) {
    return;
  }
  blogSliderArrowLeft.style.opacity = 0.6;
  blogSliderArrowRight.style.opacity = 0.6;
  blogCurrentSlide--;
  // blogSlides[blogCurrentSlide].scrollIntoView();
  blogsListELe.scrollLeft -= sliderWidth;
  if (blogCurrentSlide === 0) {
    blogSliderArrowLeft.style.opacity = 0.3;
  }

  sliding = false;
  setTimeout(() => {
    sliding = true;
  }, 500);
};

blogSliderArrowRight.addEventListener("click", nextBlogSlide);
blogSliderArrowLeft.addEventListener("click", prevBlogSlide);
