const fetchProjects = async () => {
  try {
    const response = await fetch("/api/v1/projects");
    const jsonResponse = await response.json();
    const projList = jsonResponse.data.projectsList;
    console.log(projList);

    const workOverviewList = document.querySelector(".work-overview-list");

    // First list of work-overview-card rendering functionality
    let i = 0;
    let stopRenderAt = 0;
    cardsCount = projList.length;

    function renderWorkOverviewCards() {
      stopRenderAt += 5;
      let cardsHtml = "";
      for (j = i; j < stopRenderAt; j++) {
        if (i < projList.length) {
          cardsHtml += `
        <li class="work-overview-card">
          <a href="work-detail.html" class="work-overview-img-wrapper"
            ><img src="${projList[j].imgs[0]}" alt=""
          /></a>
          <div class="work-overview-desc">
            <a href="work-detail.html" class="category-name">${projList[j].industry}</a>
            <a href="work-detail.html" class="project-title">
            ${projList[j].projectTitle}
            </a>
          </div>
        </li>
        `;
          i++;
        } else break;
      }
      workOverviewList.insertAdjacentHTML("beforeend", cardsHtml);
    }

    renderWorkOverviewCards();
  } catch (err) {
    console.log(err);
  }
};
